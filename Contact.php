                        title: 'Error!',
                        html: '<p style="font-size:14px;">' + (result || 'Something went wrong.') +
                            '</p>',
                        icon: 'error',
                        confirmButtonText: 'OK'
                    });
                }
            })
            .catch(error => {
                Swal.fire({
                    title: 'Error!',
